def train(train_loader, model, critera, optimiser, epoch):
    """
    A single epoch of training

    Args:
        train_loader: Dataloader for training
        model: model
        criteria: MultiBox Loss
        optimiser: optimiser
        epoch: the epoch number (integer)
    """

    model.train()
    batch_time = AverageMeter()
    data_time = AverageMeter()
    losses = AverageMeter()

    start = time.time()

    for i, (images, boxes, labels, _) in enumerate(train_loader):
        data_time.update(time.time() - start)
        images = images.to(device)
        boxes = [b.to(device) for b in boxes]
        labels = [l.to(device) for l in labels]

        predicted_locs, predicted_scores = model(images)

        loss = critera(predicted_locs, predicted_scores, boxes, labels)

        optimiser.zero_grad()
        loss.backward()

        if grad_clip is not None:
            clip_gradient(optimiser, grad_clip=grad_clip)

        optimiser.step()
        losses.update(loss.item(), images.size(0))
        batch_time.update(time.time() - start)

        if i % print_freq == 0:
            print('Epoch: [{0}][{1}/{2}]\t'
                  'Batch Time {batch_time.val:.3f} ({batch_time.avg:.3f})\t'
                  'Data Time {data_time.val:.3f} ({data_time.avg:.3f})\t'
                  'Loss {loss.val:.4f} ({loss.avg:.4f})\t'.format(epoch, i, len(train_loader),
                                                                  batch_time=batch_time,
                                                                  data_time=data_time, loss=losses))
    del predicted_locs, predicted_scores, images, boxes, labels




    
